using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class DropZone
{
    float width;
    float height;
    float xPos;
    float yPos;
    public DropZone(float width, float height, float xPos, float yPos)
    {
        this.width = width;
        this.height = height;
        this.xPos = xPos;
        this.yPos = yPos;

    }
}
